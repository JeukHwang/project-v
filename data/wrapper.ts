import { NODES_MAP, Node, WAYS_MAP, Way } from "./raw";

type Way_Draw = {
  id: Way["id"];
  nodes: [Node["lat"], Node["lon"]][];
  group: Way["tags"]["name"];
};

export const WAY_DRAW_MAP = new Map<number, Way_Draw>(
  [...WAYS_MAP.values()].map((way) => {
    const way_draw = {
      id: way.id,
      nodes: way.nodes.map((nodeId) => [
        NODES_MAP.get(nodeId)!.lat,
        NODES_MAP.get(nodeId)!.lon,
      ]),
      group: way.tags.name,
    } as Way_Draw;
    return [way_draw.id, way_draw];
  })
);

